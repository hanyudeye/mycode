public class Switch {
    public static void main(String[] args) {
        int s = 32;
        switch (s) {
            case 1:
                System.out.println("s 的值是 1");
                break;
            case 2:
                System.out.println("s的值是 2");
                break;
            case 3:
                System.out.println("s的值是 3");
                break;
            default:
                System.out.println("s的值是 默认的没有");

        }
    }
}
