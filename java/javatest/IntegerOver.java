public class IntegerOver {
    public static void main(String[] args) {
        int max = Integer.MAX_VALUE;
        System.out.println("最大整数:" + max);
        //这里涉及到整数编码了,要注意
        System.out.println("最大整数加1 会怎么样 ? " + (max + 1));
        System.out.println("最大整数加2 会怎么样 ? " + (max + 2));

    }
}
